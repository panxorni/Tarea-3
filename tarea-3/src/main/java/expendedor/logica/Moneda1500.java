package expendedor.logica;

/**
 * Representa una moneda con un valor de 1500.
 */
public class Moneda1500 extends Moneda {

    /**
     * Constructor de la moneda de 1500.
     */
    public Moneda1500() {
        super();
    }

    /**
     * Retorna el valor específico de esta moneda.
     *
     * @return 1500.
     */
    @Override
    public int getValor() {
        return 1500;
    }
}